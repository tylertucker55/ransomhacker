package byui.cit260.ransomhacker.model;

import java.io.Serializable;
import java.util.Objects;
/**
 *
 * @author tylertucker
 */
public class Job implements Serializable {

    private double pay;
    private String name;
    private String description;
    
    
    
    public Job() {
    }

    public double getPay() {
        return pay;
    }

    public void setPay(double pay) {
        this.pay = pay;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @Override
    public int hashCode() {
        int hash = 5;
        hash = 29 * hash + (int) (Double.doubleToLongBits(this.pay) ^ (Double.doubleToLongBits(this.pay) >>> 32));
        hash = 29 * hash + Objects.hashCode(this.name);
        hash = 29 * hash + Objects.hashCode(this.description);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Job other = (Job) obj;
        if (Double.doubleToLongBits(this.pay) != Double.doubleToLongBits(other.pay)) {
            return false;
        }
        if (!Objects.equals(this.name, other.name)) {
            return false;
        }
        if (!Objects.equals(this.description, other.description)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Job{" + "pay=" + pay + ", name=" + name + ", description=" + description + '}';
    }
    
    
    
}